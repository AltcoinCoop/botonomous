<?php

namespace Botonomous;

/**
 * Class Channel.
 */
class Channel extends AbstractSlackEntity
{
    private $name;

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }
}
